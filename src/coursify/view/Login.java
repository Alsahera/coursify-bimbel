    private void performLogin() {
        String username = UsernameField.getText().trim();
        // Catatan: Gunakan PasswoardField (dengan double 'a') sesuai deklarasi NetBeans
        String password = new String(PasswoardField.getPassword());
        
        if (username.isEmpty() || password.isEmpty()) {
            pemberitahuan.setText("Username dan password tidak boleh kosong!");
            pemberitahuan.setForeground(ERROR_COLOR);
            return;
        }
        
        // Cari user di DataStore (Menggunakan kelas DataStore dari file yang Anda unggah)
        User user = DataStore.getUser(username, password);
        
        if (user != null) {
            // Login berhasil
            pemberitahuan.setText("Login berhasil! Selamat datang " + user.getUsername());
            pemberitahuan.setForeground(SUCCESS_COLOR);
            
            // Polymorphism: Tampilkan frame yang sesuai berdasarkan role
            SwingUtilities.invokeLater(() -> {
                if (user instanceof Admin) {
                    // Jika Admin, tampilkan AdminMainFrame
                    AdminMainFrame adminFrame = new AdminMainFrame((Admin) user);
                    adminFrame.setVisible(true);
                } else if (user instanceof Tutor) {
                    // Jika Tutor, tampilkan TutorMainFrame
                    TutorMainFrame tutorFrame = new TutorMainFrame((Tutor) user);
                    tutorFrame.setVisible(true);
                }
                dispose(); // Tutup login frame
            });
        } else {
            // Login gagal
            pemberitahuan.setText("Username atau password salah!");
            pemberitahuan.setForeground(ERROR_COLOR);
            PasswoardField.setText("");
        }
    }
    
